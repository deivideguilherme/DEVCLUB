/*
OPERADORES ARITMÉTICOS

    +  => Adição 
    -  => Subtração
    /  => Divisão
    *  => Multiplicação
    %  => Resto
    ++ => Incremento
    -- => Decremento
    ** => Exponêncial

*/

const adicao = 3+1
    console.log(adicao)

const subtracao = 1-4
    console.log(subtracao)

const divisao = 4/2
    console.log(divisao)

const multiplicacao = 3*9
    console.log(multiplicacao)

const resto = 7%5
    console.log(resto)

let incremento = 20 //Usado a variável como let, pois const não pode ser altarada! 
    incremento++
    console.log(incremento)

let decremento = 20 //Usado a variável como let, pois const não pode ser altarada! 
    incremento--
    console.log(decremento)

const exponencial = 2 ** 3 //2³ dois elevado a terceira potência
    console.log(exponencial)