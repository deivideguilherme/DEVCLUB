/*
OPERADORES DE ATRIBUIÇÃO --> FORMAS DE ECONOMIZAR CÓDIGO

     = --> Atribuição
    += --> Adição
    -= --> Subtração
    /= --> Divisão
    *= --> Multiplicação
    %= --> Resto

*/
const result = 20 //Atribuição
    result += 70  //Adição  --> é o mesmo que tivesse feito assim: result = result + 70
    result -= 70  //Subtração
    result /= 70  //Divisão
    result *= 70  //Multiplicação
    result %= 70  //Resto